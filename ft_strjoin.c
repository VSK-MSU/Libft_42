#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*ans;

	if (!s1 && !s2)
		return (ft_strdup(""));
	if (!s1)
		return (ft_strdup(s2));
	if (!s2)
		return (ft_strdup(s1));
	ans = ft_strdup(s1);
	if (!ans)
		return (NULL);
	ft_strlcat(ans, s2, ft_strlen(ans) + ft_strlen(s2) + 1);
	return (ans);
}
